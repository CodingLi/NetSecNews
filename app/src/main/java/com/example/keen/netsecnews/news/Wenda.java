public class Wenda {
    private String wenda_title;
    private String wenda_digest;
    private String wenda_ptime;
    private int wenda_commentcount;

    public String getWenda_title() {
        return wenda_title;
    }

    public void setWenda_title(String wenda_title) {
        this.wenda_title = wenda_title;
    }

    public String getWenda_digest() {
        return wenda_digest;
    }

    public void setWenda_digest(String wenda_digest) {
        this.wenda_digest = wenda_digest;
    }

    public String getWenda_ptime() {
        return wenda_ptime;
    }

    public void setWenda_ptime(String wenda_ptime) {
        this.wenda_ptime = wenda_ptime;
    }

    public int getWenda_commentcount() {
        return wenda_commentcount;
    }

    public void setWenda_commentcount(int wenda_commentcount) {
        this.wenda_commentcount = wenda_commentcount;
    }
}
